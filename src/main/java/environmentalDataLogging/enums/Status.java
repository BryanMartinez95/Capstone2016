package environmentalDataLogging.enums;

/**
 * Enumeration to describe the device's status.
 * ACTIVE:      the device is currently active and shown by default
 * INACTIVE:    the device is currently inactive and is hidden by default
 */
public enum Status
{
    /**
     * Active status.
     */
    ACTIVE, /**
     * Inactive status.
     */
    INACTIVE, /**
     * All status.
     */
    ALL
}
