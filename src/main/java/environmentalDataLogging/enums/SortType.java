package environmentalDataLogging.enums;

/**
 * An enum to identify the type of sort on the grid
 * ASCENDING: sort the grid by ascending order
 * DESCENDING: sort the grid by descending order
 * NONE: none identified
 */
public enum SortType
{
    /**
     * Ascending sort type.
     */
    ASCENDING, /**
     * Descending sort type.
     */
    DESCENDING, /**
     * None sort type.
     */
    NONE
}
